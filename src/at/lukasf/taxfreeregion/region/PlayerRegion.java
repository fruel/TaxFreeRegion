package at.lukasf.taxfreeregion.region;

import java.io.Serializable;

public class PlayerRegion implements Serializable {
	private static final long serialVersionUID = 6603347582218776939L;

	String player, region;

	public PlayerRegion(String player, String region) {
		this.player = player;
		this.region = region;
	}

	public String getPlayer() {
		return player;
	}

	public String getRegion() {
		return region;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((player == null) ? 0 : player.hashCode());
		result = prime * result + ((region == null) ? 0 : region.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PlayerRegion other = (PlayerRegion) obj;
		if (player == null) {
			if (other.player != null)
				return false;
		} else if (!player.equals(other.player))
			return false;
		if (region == null) {
			if (other.region != null)
				return false;
		} else if (!region.equals(other.region))
			return false;
		return true;
	}

}
